// Add new bullet types easier
public enum BulletType {
    BASIC, // 1 - Standard bullet
    FIRE, // 2 - Damage over time
    FREEZE, // 3 - Slows enemies
    ELECTRIC, // 4 - Chains to nearby enemies
    SPIRIT, // 5 - Pushes enemies back
    RAPID, // 6 - Fast, low cooldown
    PIERCING, // 7 - Passes through all enemies
    EXPLOSIVE, // 8 - Area-of-effect blast
    TELEPORT // 9 - Moves enemy away from treasure
}
